public class JavaDocumentation {
    public static void main(String[] args) {
        System.out.print("The sine of 15: ");
        System.out.println(Math.sin(15));
        System.out.print("63^2.5: ");
        System.out.println(Math.pow(63, 2.5));
        System.out.print("sqrt(2498): ");
        System.out.println(Math.sqrt(2498));
        System.out.print("Random positive number < 1: ");
        System.out.println(Math.random());
        System.out.print("Pi: ");
        System.out.println(Math.PI);
    }
}
